function draw_axis(name, data1, t)
Fig = figure(...
    'Units',           'pixels',...
    'Name',            name,...
    'NumberTitle',     'off',...
    'IntegerHandle',   'off');
% 创建axes对象, 设定坐标轴属性
AxesH = axes(...
    'Parent',  Fig,...
    'Xlim',    [0 60],...
    'XGrid',   'on',...
    'YGrid',   'on',...
    'Visible', 'on');


realtrace = line(AxesH, t, data1, 'linewidth',2);
precisetrace = line(AxesH, [0 60], [9.7982 9.7982],...
                    'color','r',...
                    'linewidth',2,...
                    'linestyle','--');
xlabel(AxesH, 't/min');
ylabel(AxesH, 'g/(m/s^2)');
legend(AxesH,[realtrace,precisetrace],...
    'Measured Gravity','Benchmark Value',...
    'location','northwest');
% 指定保存路径和格式
saveas(Fig,['E:\E盘\论文相关\IEEE_Sensor_Journal\simu_code\simu\image\' Fig.Name],'pdf')
end